public class Task2 {
	
	
	public static void main(String[] args) {
		

		// width 5, height 8 and whats the area?
		
		// declare h and w;
		
		int h = 8;
		int w = 5;
		
		
		//declare perimeter and area variables
		
		int p, a;
		
	 p = (2*h) + (2*w);
	 a = h*w;
		
		//calculate perimeter 
		System.out.println(p);
		//calculate area
		System.out.println(a);
		//print the sentence
		
		System.out.println("The perimeter of a rectangle with width " + w + " and height " + h + " is equal to " + p + 
				" and" + " the area is " + a +".");
		
		//formula for declaring is datatype - identifier - operator - value
		
	}

}
