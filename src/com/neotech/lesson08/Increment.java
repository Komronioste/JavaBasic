package com.neotech.lesson08;

public class Increment {

	public static void main(String[] args) {
		
		
		
		int x = 10;
		
		x+=1;
		
		x= x+1; //shorthand operator
		
		
		x++;  // increment.  same as x = x + 1;
		
		System.out.println(x);
		
		int y = 15;
		
		y = y-1;
		
		y-=1;
		
		y--;   //decrement
		
		--y; //post decrement
		
		System.out.println(y);
		

	}

}
