package com.neotech.lesson08;

public class IntroToForLoop {

	public static void main(String[] args) {
	
		
		int a = 1;
		
		while (a<=10)
		{
			System.out.println(a+ " ");
			a++;
		}
		

		// USING FOR LOOP 
		// for loop - is better, when you know how many times you need to loop/iterate.
		
		for (int num = 1; num<=10; num++ )    // we can declare the variable right in the condition along with condition and increment
			
		{
			
			System.out.print(num+ " ");    //print will print horizontally, println will print vertically.
			
			
		}
		
		
		
		
		
		
		
		
	}

}
