package com.neotech.lesson02;

public class ReAssigningValue {
	
public static void main(String[] args) {
	
	//1.declaring
	int number1;
	
	//System.out.println(number1); // if we do not end up assigning value to number1, compiler will give an error.
	//2. assigning
	number1 = 92;
	System.out.println(number1);
	
	//3.re assigning
	number1=273;
	System.out.println(number1);
}

}
