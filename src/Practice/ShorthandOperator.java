package Practice;

public class ShorthandOperator {

	
	public static void main(String[] args) {
		
		
		int num1 = 100;
		num1 +=100;
		System.out.println(num1);
		
		num1 -=67;
		System.out.println(num1);
		
		
		double cakePiece = 11;
		cakePiece /=4;
		System.out.println(cakePiece);

		int cake = 25;
		cake %=7;   // looking for modulus(remainder) after dividing cake between 7 people.
		System.out.println(cake);  
		
	}
}
